        }

        private T profile<T>(Func<T> func)
        {
            var stopwatch = new Stopwatch();
            stopwatch.Start();
            var result = func();
            stopwatch.Stop();
            Assert.LessOrEqual(stopwatch.ElapsedMilliseconds, _timeLimit, $"Time limit exceeded. {stopwatch.ElapsedMilliseconds}ms");
            return result;
        }
