        private void Profile(SolDelegate solutionMethod, string num, int target, out IList<string> actual, out long duration)
        {

            // initialize stopwatch
            var sw = new Stopwatch();
            sw.Start();

            // call the solution method
            actual = solutionMethod(num, target);

            // stop the stopwatch
            sw.Stop();
            duration = sw.ElapsedMilliseconds;


        }
