        [Test]
        [TestCaseSource(nameof(testCases))]
        public void Test1(string testCaseName, string nums, int target, List<string> expected)
        {

            IList<string> actual;
            long duration;
            if (solution.EnableDebug) Trace.WriteLine(string.Format("TestCase: {0}", testCaseName));
            Profile(solution.AddOperators, nums, target, out actual, out duration);

            var actualSet = new SortedSet<string>(actual);
            var expectedSet = new SortedSet<string>(expected);
            CompareSets(expectedSet, actualSet);

            Assert.Less(duration, _maxDuration);
        }

        private void CompareSets(SortedSet<string> expectedSet, SortedSet<string> actualSet)
        {

            foreach (var item in expectedSet) Assert.Contains(item, actualSet);
        }

